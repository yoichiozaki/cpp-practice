#include <bits/stdc++.h>
using namespace std;

int main()
{
    int n;
    cin >> n;
    if (100 <= n && n <= 111)
        cout << 111 << endl;
    else if (112 <= n && n <= 222)
        cout << 222 << endl;
    else if (223 <= n && n <= 333)
        cout << 333 << endl;
    else if (334 <= n && n <= 444)
        cout << 444 << endl;
    else if (445 <= n && n <= 555)
        cout << 555 << endl;
    else if (556 <= n && n <= 666)
        cout << 666 << endl;
    else if (667 <= n && n <= 777)
        cout << 777 << endl;
    else if (778 <= n && n <= 888)
        cout << 888 << endl;
    else if (889 <= n && n <= 999)
        cout << 999 << endl;
    return 0;
}